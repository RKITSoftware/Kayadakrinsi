-- GROUP BY
SELECT
	COUNT(P01F01),P01F03
FROM
	SUP01
GROUP BY
	P01F03;

-- HAVING
SELECT
	COUNT(P01F01),P01F03,P01F04
FROM
	SUP01
GROUP BY
	P01F03,P01F04
HAVING
	P01F03 = 'RAJKOT';
    
-- LIMIT
SELECT
	P01F01,
    P01F02,
    P01F03,
    P01F04
FROM
	SUP01
ORDER BY
	P01F03
LIMIT 4;

-- OFFSET
SELECT
	P01F01,P01F02,P01F03,P01F04
FROM
	SUP01
