-- AUTO INCREMENT
CREATE TABLE SUP01(
	P01F01 INT NOT NULL PRIMARY KEY AUTO_INCREMENT COMMENT 'SUPPLIER ID',
    P01F02 VARCHAR(30) COMMENT 'SUPPLIER NAME',
    P01F03 VARCHAR(30) DEFAULT 'RAJKOT' COMMENT 'SUPPLIER CITY',
    P01F04 VARCHAR(30) DEFAULT 'INDIA' COMMENT 'SUPPLIER COUNTRY'
);

