CREATE DATABASE 
	HOSPITAL_MANAGEMENT;

CREATE TABLE STFF01(
	F01F01 INT NOT NULL PRIMARY KEY AUTO_INCREMENT COMMENT 'DOCTOR ID',
    F01F02 VARCHAR(25) COMMENT 'DOCTOR NAME',
    F01F03 VARCHAR(25) COMMENT 'DOCTOR QUALIFICATION',
    F01F04 ENUM('SUNDAY','MONAY','TUESDAY','WEDNESDAY','THURSDAY','FRIDAY','SATURDAY') NOT NULL COMMENT 'DOCTOR WORKING DAYS'
);

RENAME TABLE STFF01 TO STF01;

DROP TABLE 
	STF01;

CREATE TABLE STF01(
	F01F01 INT NOT NULL PRIMARY KEY AUTO_INCREMENT COMMENT 'DOCTOR ID',
    F01F02 VARCHAR(25) COMMENT 'DOCTOR NAME',
    F01F03 VARCHAR(25) COMMENT 'DOCTOR QUALIFICATION',
    F01F04 SET('0','1','2','3','4','5','6') NOT NULL COMMENT 'DOCTOR WORKING DAYS'
);

CREATE TABLE STF02(
	F02F01 INT NOT NULL PRIMARY KEY AUTO_INCREMENT COMMENT 'HELPER ID',
    F02F02 VARCHAR(25) COMMENT 'HELPER NAME',
    F02F03 ENUM('NURSE', 'CLINICAL_ASSISTANT', 'PATIENT_SERVICE_ASSISTANT', 'WARD_CLERKS', 'VOLUNTEER')  NOT NULL COMMENT 'HELPER ROLE',
    F02F04 SET('0','1','2','3','4','5','6') NOT NULL COMMENT 'HELPER WORKING DAYS'
);
    
CREATE TABLE DIS01(
	S01F01 INT NOT NULL PRIMARY KEY AUTO_INCREMENT COMMENT 'DIEASES ID',
    S01F02 VARCHAR(25) COMMENT 'DIEASES NAME',
    S01F03 INT COMMENT 'DOCTOR ID',
    FOREIGN KEY (S01F03) REFERENCES STF01(F01F01)
);

CREATE TABLE PTN01(
	N01F01 INT NOT NULL PRIMARY KEY AUTO_INCREMENT COMMENT 'PATIENT ID',
    N01F02 VARCHAR(25) COMMENT 'PATIENT NAME',
    N01F03 BIGINT(10) COMMENT 'PATIENT MOBILE',
    N01F04 INT COMMENT 'DIEASES ID',
    FOREIGN KEY (N01F04) REFERENCES DIS01(S01F01)
);

CREATE TABLE CHRG01(
	G01F01 INT NOT NULL PRIMARY KEY AUTO_INCREMENT COMMENT 'CHARGE ID',
    G01F02 INT COMMENT 'DOCTOR ID',
    G01F03 INT COMMENT 'DIEASES ID',
    G01F04 DECIMAL(10,2) COMMENT 'CHARGE DOCTOR',
    FOREIGN KEY (G01F02) REFERENCES STF01(F01F01),
    FOREIGN KEY (G01F03) REFERENCES DIS01(S01F01)
);

CREATE TABLE RCD01(
	D01F01 INT NOT NULL PRIMARY KEY AUTO_INCREMENT COMMENT 'RECORD ID',
	D01F02 INT COMMENT 'PATIENT ID',
    D01F03 INT COMMENT 'DOCTOR ID',
    D01F04 INT COMMENT 'HELPER ID',
    D01F05 INT COMMENT 'DIEASES ID',
    D01F06 INT COMMENT 'CHARGE ID',
    D01F07 DATE COMMENT 'ADMIT DATE',
    D01F08 DATE COMMENT 'DISCHARGE DATE',
    FOREIGN KEY (D01F02) REFERENCES PTN01(N01F01),
    FOREIGN KEY (D01F03) REFERENCES STF01(F01F01),
    FOREIGN KEY (D01F04) REFERENCES STF02(F02F01),
    FOREIGN KEY (D01F05) REFERENCES DIS01(S01F01),
    FOREIGN KEY (D01F06) REFERENCES CHRG01(G01F01)
);
	

    